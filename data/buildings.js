function studyWork(
  name,
  parentName,
  description,
  actionLabel,
  purchaseMessage,
  baseCost,
  costMultiplier,
  baseProduction,
  unlock,
  ...effects
) {
  return {
    name,
    parentTier: getParentTier(parentName),
    parentName,
    description,
    actionLabel,
    purchaseMessage,
    baseCost,
    costMultiplier,
    baseProduction,
    unlock,
    effects,
  };
}

function getParentTier(parentName) {
  return parentName
    .replace(/\s+(.)/g, (_, letter) => letter.toUpperCase())
    .replace(/^./, (letter) => letter.toLowerCase());
}

function reached(amount) {
  return {
    type: "understandingReached",
    amount,
  };
}

function owned(buildingId, amount) {
  return {
    type: "buildingOwned",
    buildingId,
    amount,
  };
}

function researched(upgradeId) {
  return {
    type: "upgradePurchased",
    upgradeId,
  };
}

function synergy(target, source, valuePerOwned) {
  return {
    type: "buildingSynergyProductionMultiplier",
    target,
    source,
    valuePerOwned,
  };
}
